   public static String readFromFile(String filePath){
       BufferedReader br = null;
       String out = "";
       try {
           File file = new File(filePath);
           br = new BufferedReader(new FileReader(file));
           String line = null;
            while((line = br.readLine()) != null){
                out += line+"\n"; 
            }
       } catch(IOException e){
           e.printStackTrace();
       } finally {
           try {
               br.close();
           } catch (Exception e){}
       
       }
   
   
      return out;
   }
   
